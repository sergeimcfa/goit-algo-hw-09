def find_coins_greedy(amount):
    """
    Жадібний алгоритм для видачі решти.
    
    :param amount: Сума, для якої потрібно видати решту.
    :return: Словник {номінал: кількість}, що представляє монети.
    """
    # Доступні номінали монет (обов'язково відсортовані за спаданням для жадібного підходу)
    coins = [50, 25, 10, 5, 2, 1]
    
    # Словник для збереження результату
    change = {}
    
    for coin in coins:
        # Визначаємо, скільки монет поточного номіналу можна взяти
        count = amount // coin
        
        # Якщо кількість більше нуля, додаємо в результат
        if count > 0:
            change[coin] = count
            # Оновлюємо залишок суми, який ще треба видати
            amount = amount % coin
            
    return change

if __name__ == "__main__":
    # Тестова сума
    amount_to_change = 113
    
    print(f"--- Жадібний алгоритм ---")
    print(f"Сума для розміну: {amount_to_change}")
    
    # Виклик функції
    result = find_coins_greedy(amount_to_change)
    
    print(f"Результат: {result}")
    # Очікується: {50: 2, 10: 1, 2: 1, 1: 1}
